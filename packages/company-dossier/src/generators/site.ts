/** Strip a leading YAML frontmatter block from generated markdown, if any. */
function stripFrontmatter(content: string): string {
  if (content.startsWith('---\n')) {
    const end = content.indexOf('\n---', 4);
    if (end !== -1) {
      const after = content.indexOf('\n', end + 1);
      return after !== -1 ? content.slice(after + 1) : '';
    }
  }
  return content;
}

/**
 * Extract a human title from a dossier markdown body.
 * Section files start with `# 1. Overview & Identity — Acme`; we keep the
 * descriptive part ("Overview & Identity") and drop the leading number and
 * the " — Company" suffix so the sidebar reads cleanly.
 */
function extractTitle(content: string, fallback: string): string {
  const m = content.match(/^#\s+(.+?)\s*$/m);
  if (!m) return fallback;
  let t = m[1].trim();
  t = t.replace(/^\d+\.\s*/, ''); // drop "1. "
  t = t.replace(/\s*[—–-]\s*[^—–-]+$/, ''); // drop " — Company"
  return t.trim() || fallback;
}

/** Derive a numeric order from a filename like "03_hiring_radar.md". */
function orderFromFilename(filePath: string): number | undefined {
  const m = path.basename(filePath).match(/^(\d+)/);
  return m ? parseInt(m[1], 10) : undefined;
}

/** Escape a value for safe single-line YAML (quote + escape quotes). */
function yamlString(value: string): string {
  return '"' + value.replace(/\\/g, '\\\\').replace(/"/g, '\\"') + '"';
}

function isOverview(filePath: string): boolean {
  const b = path.basename(filePath).toLowerCase();
  return b === 'readme.md' || /^0*1[_.]/.test(b) || b.includes('overview');
}

