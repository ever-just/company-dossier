function buildPackageJson(name: string, deploy: boolean): string {
  const pkg: Record<string, unknown> = {
    name: `${slugify(name) || 'company'}-dossier-site`,
    private: true,
    version: '0.0.1',
    type: 'module',
    scripts: {
      dev: 'astro dev',
      build: 'astro build',
      preview: 'astro preview',
      ...(deploy ? { deploy: 'gh-pages -d dist -t true' } : {}),
    },
    dependencies: {
      astro: ASTRO_VERSION,
      '@astrojs/starlight': STARLIGHT_VERSION,
    },
    // gh-pages is an optional dep of the GENERATED site, not the main package.
    optionalDependencies: {
      'gh-pages': GH_PAGES_VERSION,
    },
  };
  return JSON.stringify(pkg, null, 2) + '\n';
}

function buildAstroConfig(title: string, noindex: boolean): string {
  const headEntry = noindex
    ? `
  head: [
    {
      tag: 'meta',
      attrs: { name: 'robots', content: 'noindex,nofollow' },
    },
  ],`
    : '';
  return `import { defineConfig } from 'astro/config';
import starlight from '@astrojs/starlight';

// Generated by company-dossier (--format site).
// Per policy, per-company dossier sites are UNLISTED + NOINDEX by default.
export default defineConfig({
  integrations: [
    starlight({
      title: ${yamlString(title)},
      description: ${yamlString(DISCLAIMER)},
      tableOfContents: true,
      customCss: ['./src/styles/case-file.css'],${headEntry}
      // Sidebar autogenerated from the docs tree.
      sidebar: [
        {
          label: 'Dossier',
          autogenerate: { directory: '.' },
        },
      ],
      // Footer disclaimer + takedown line on every page.
      credits: false,
      lastUpdated: false,
      pagination: false,
    }),
  ],
});
`;
}

function buildContentConfig(): string {
  // LOOSE schema: extend Starlight's docs schema with optional extra keys so
  // generated frontmatter always validates. (A plain `.passthrough()` object
  // can't be intersected/merged with Starlight's hero schema, so we declare
  // tolerant optional fields instead — these cover the keys generated content
  // may carry now or later, and unknown-but-typed keys are accepted.)
  return `import { defineCollection } from 'astro:content';
import { docsLoader } from '@astrojs/starlight/loaders';
import { docsSchema } from '@astrojs/starlight/schema';
import { z } from 'astro:content';

export const collections = {
  docs: defineCollection({
    loader: docsLoader(),
    schema: docsSchema({
      // Tolerant extra frontmatter keys for generated content.
      extend: z.object({
        order: z.number().optional(),
        section: z.string().optional(),
        sourceFile: z.string().optional(),
        generatedAt: z.string().optional(),
        company: z.string().optional(),
        domain: z.string().optional(),
        customNote: z.string().optional(),
      }),
    }),
  }),
};
`;
}

