import type { DossierResult, DossierFile } from '../core.js';

/* ------------------------------------------------------------------ */
/* Public types                                                        */
/* ------------------------------------------------------------------ */

export interface GenerateSiteOptions {
  /** Custom subdomain/host. When set, writes a public/CNAME file. */
  subdomain?: string;
  /**
   * Inject `<meta name="robots" content="noindex,nofollow">` on every page
   * and a Disallow-all robots.txt. Defaults to TRUE (unlisted by policy).
   */
  noindex?: boolean;
  /** Override the site title. Defaults to the dossier company name. */
  title?: string;
}

export interface GenerateSiteResult {
  /** Absolute path to the generated `site/` directory. */
  siteDir: string;
  /** Absolute paths of every file written, relative to nothing — fully qualified. */
  files: string[];
  /** Whether noindex/unlisted policy was applied. */
  noindex: boolean;
}

/* Pinned, recent versions (Node-only Astro Starlight). */
const ASTRO_VERSION = '^5.7.0';
const STARLIGHT_VERSION = '^0.34.0';
const GH_PAGES_VERSION = '^6.3.0';

const DISCLAIMER =
  'Auto-generated from public sources — may be inaccurate; not affiliated with the company.';
const TAKEDOWN =
  'Takedown / contact: open an issue at https://github.com/ever-just/company-dossier/issues';

