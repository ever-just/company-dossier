function buildRobotsTxt(noindex: boolean): string {
  if (noindex) {
    return 'User-agent: *\nDisallow: /\n';
  }
  return 'User-agent: *\nAllow: /\n';
}

/** Markdown disclaimer banner embedded in every generated doc. */
function disclaimerMarkdown(): string {
  return `:::caution[Disclaimer]
**CONFIDENTIAL — UNVERIFIED.** ${DISCLAIMER}

${TAKEDOWN}
:::

`;
}

/** Build the index.md (hero + overview) from the dossier overview/README. */
function buildIndexDoc(result: DossierResult, files: DossierFile[], title: string): string {
  const overviewFile =
    files.find((f) => path.basename(f.path).toLowerCase() === 'readme.md') ||
    files.find((f) => isOverview(f.path));
  const body = overviewFile ? stripFrontmatter(overviewFile.content) : '';
  // Drop the first top-level heading; the hero supplies the title.
  const bodyNoH1 = body.replace(/^#\s+.+?\r?\n/, '');

  const frontmatter = [
    '---',
    `title: ${yamlString(title)}`,
    'template: splash',
    'hero:',
    `  title: ${yamlString(title)}`,
    `  tagline: ${yamlString('Intelligence dossier compiled from public sources.')}`,
    '  actions:',
    `    - text: Read the dossier`,
    `      link: ./overview-identity/`,
    `      icon: right-arrow`,
    '---',
    '',
  ].join('\n');

  return frontmatter + disclaimerMarkdown() + bodyNoH1.trim() + '\n';
}

/** Build one Starlight doc from a dossier section file. */
function buildSectionDoc(file: DossierFile, title: string, order: number | undefined): string {
  const body = stripFrontmatter(file.content).replace(/^#\s+.+?\r?\n/, '');
  const fm: string[] = ['---', `title: ${yamlString(title)}`];
  if (order !== undefined) {
    fm.push('sidebar:', `  order: ${order}`);
  }
  fm.push('---', '');
  return fm.join('\n') + disclaimerMarkdown() + body.trim() + '\n';
}

